using UnityEngine;
using System.Collections;

public class Potion {

	private int amount;

	public int Amount{

		get{
			return amount;
		}set{
			amount = value;
		}

	}

	private string potionName;

	public string PotionName{

		get{
			return potionName;
		}set{
			potionName = value;
		}

	}

}
